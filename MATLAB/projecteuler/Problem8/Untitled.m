%Find the thirteen adjacent digits in the 1000-digit number that have the greatest product. 
%What is the value of this product?
clc;clear;
load('matlab.mat');
newdata=zeros(1,1000);
for i=1:20
    for j=1:5
        for k=1:10
            newdata(50*(i-1)+10*(j-1)+k)=mod(floor(data(i,j)/10^(10-k)),10);
        end
    end
end
len=length(newdata);
% newdata is the data that has been handled again,
% as to in this problem use it more correct.

clear i j k 
gestpro=0;
adjcount=13;
for i=1:(len-adjcount)
    product=1;
    for j=1:adjcount
        product=product*newdata(i+j);
    end
    if product>gestpro
        gestpro=product;
    end
end
